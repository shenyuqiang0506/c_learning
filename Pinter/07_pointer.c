#include <stdio.h>
//指针的运算
int main() {
    /*
   指针的运算
    有意义的操作:
        指针跟整数进行加、减操作(每次移动N个步长)
        指针跟指针进行减操作(间隔步长)
    无意义的操作:
        指针跟整数进行乘除操作
        原因:此时指针指向不明
    指针跟指针进行加、乘、除操作
     */
    //前提条件：内存空间是连续的
    int arr[] = {1, 2, 3, 4, 5};
    int *p1 = &arr[0];
    printf("%d\n", *p1);
    printf("%d\n", *(p1 + 1));
    printf("%d\n", *(p1 + 2));

    int *p2 = &arr[3];
    printf("%ld\n", p2 - p1);
}
